package org.example;

public class D {
    int a;
    double b;
    String s;

    public D(int a, double b, String s) {
        this.a = a;
        this.b = b;
        this.s = s;
    }

    public int getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public String getS() {
        return s;
    }
}
